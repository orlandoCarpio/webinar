        <h3>¿Quien soy?</h3>
        <div class="foto-per">
            <img src="http://localhost/webinar/wp-content/uploads/2020/09/IMG_3727.jpg" alt="">
        </div>
        <p>Siempre pensé que definirse es limitarse pero entiendo que hace falta una presentación.</p>
        <p>Soy Terapeuta en Medicina <em>Ayurveda</em> y Artista Plástica.</p>
        <p>Me considero una eterna buscadora que cree en el arte como el camino del espíritu y en el estudio como el camino hacia el autoconocimiento, por eso mi vida siempre fue y es una combinación entre ambos mundos: arte y estudio.</p>
        <p>Comencé a interesarme por la filosofía India y la pintura a los 11 años, por la nutrición a los 14 y por el yoga a los 17.
        Estudié medicina, ciencias biológicas, nutrición funcional y me recibí de Licenciada en Artes Plásticas en la Escuela Nacional de Bellas Artes, pero algo me faltaba...</p>
        <p>Un dia me hablaron de la medicina <em>Ayurveda</em> y desde ese momento me inicie en este mundo que une mis pasiones: el cuerpo, la mente y el alma.</p>
        <p>Estudie en Uruguay,me forme en India en <strong>PDI Prakash Institute</strong> y en <strong>PVA ayurvedic Hospital en Kerala</strong>, realice varios postgrados tanto en India como en Argentina y también me formé como terapeuta transformacional femenina y biodecodificadora.</p>
        <p>Creo que todos los caminos hablan de lo mismo pero cada ser necesita de diferentes abordajes.</p>
        <p>Manejo una cuenta en Instagram <a href="https://www.instagram.com/eatfitmaria/?hl=es-la"> @eatfitmaria</a> donde cree una comunidad para poder expandir este conocimiento a todos los que estén en la búsqueda.</p>
        <p>Atiendo consultas personales en mi consultorio virtual y dicto talleres y seminarios en varios países del mundo: <strong>India, Argentina, Chile y Uruguay</strong>.</p>
        <p>Creo que los cambios suceden muy rápido cuando uno lo desea, todas las herramientas están dentro nuestro.</p>
        <p>Conocer nuestra naturaleza nos abre las puertas del entendimiento y la transformación.</p>
        <p>Bienvenidos a mi mundo y al <em>Ayurveda</em>.</p>
